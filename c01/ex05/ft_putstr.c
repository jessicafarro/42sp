#include <unistd.h>

void	ft_putstr(char *str)
{
    int i;
    while(str[i] != '\0')
        write(1, &str[i++], 1);
}
int main() 
{
    char arr[] = {'4','2','-','F','T','W', '\n'};
	ft_putstr(arr);
	return 0;
}